package Day_03_25_09_30_Variables;

public class Demo1 {

    // Variables in Java - One line comment using forward slash

    /*
     * Multi line comment
     * 
     * What is a variable?
     * A variable is a named memory location used to store data.
     * One variable can hold one value at a time.
     */

    // Syntax: datatype variableName = variableValue;

    public static void main(String[] args) {

        // Steps to use variables in Java
        // 1. Variable declaration -- Reserve memory
    	
        int age; // dataType variableName

        // 2. Initialization - Assign value
        age = 40; // variableName = variableValue

        // 3. Usage
        System.out.println(age);
    }
}
